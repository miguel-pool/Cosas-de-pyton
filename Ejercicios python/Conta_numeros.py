def contar_numeros(lista, numero):
    # Contamos cuántas veces aparece el número en la lista
    cantidad = lista.count(numero)
    
    # Si el número aparece más de una vez, lo imprimimos en singular o plural
    if cantidad == 1:
        print(f"En la lista hay {cantidad} {numero}")
    else:
        print(f"En la lista hay {cantidad} {numero}s")

