a = 10
b = 5

#SUMA +
resultado_su = a + b
print("La suma de esto es",resultado_su)

#RESTA -
resultado_res = a - b
print("La resta de estos dos numeros es ",resultado_res)

#DIVISION /
resultado_div = a / b
print("La respuesta al dividir es ",resultado_div)

#MULTIPLICACION *
resultado_mul = a * b
print("La multiplicacion de estos dos nmeros es ",resultado_mul)

#DIVISION ENTERA // (division siempre te tira flotante)
resultado_div_ent = a // b
print("La division entera de estos 2 numeros es ",resultado_div_ent)

#MODULO %
resultado_modu = a % b
print("El modulo de esta operacion es ",resultado_modu)

#POTENCIA **
resultado_poten = a ** b
print("La potencia es ",resultado_poten)