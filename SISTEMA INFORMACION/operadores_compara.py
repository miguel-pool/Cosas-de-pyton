a = 5
b = 10

#Mayor que
mayorque = a > b
print("esto sera un resultado ",mayorque)

#Menor que
menorque = a < b
print("esto sera un resultado ",menorque)

#Igual a
igualque = a == b
print("esto sera un resultado ",igualque)

#Diferente de
diferente = a != b
print("esto sera un resultado ",diferente)

#Menor o igual
menor_igual = a <= b
print("esto sera un resultado ",menor_igual)

#Mayor o igual
mayor_igual = a >= b
print("esto sera un resultado ",mayor_igual)

